# Tipo int e float
# int -> número inteiro
"""print(11) int
print(-11) int
print(0) int"""

# O tipo int repreenta qualquer numero positivo ou negativo
# int sem sinal é considerado positivo
print(1.1, 10.11)
print(0.0, -1.5)

# float -> numero com ponto flutuante
# o tipo float representa qualquer numero
# Positivo ou negativo com ponto flutuante
# A função type mostra o tipo do python que interferiu ao valor
print(type("Cleber"))
print(type(-1))
print(type(1.1), type(-1.1),type(0.0))